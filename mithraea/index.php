            <p>Because of the vast territory the empire covered, Mithraeum remnants have been found over a great geographic area. As Christianity grew in popularity, a few elements of Mithraism – including the celebration of Dec. 25th as the birthday of the divine being – found new homes in the growing Christian faith.</p>
